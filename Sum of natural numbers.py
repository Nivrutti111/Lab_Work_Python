#Q.4)WAP to sum first 10 natural number. 

# Initialize the sum variable to 0
numbers = 0

# Use a for loop to iterate through the first 10 natural numbers
for i in range(1, 11):
    numbers += i  # Add each number to sum_of_numbers

# Print the result
print(f"The sum of the first 10 natural numbers is: {numbers}")
